package smartanalytics.diksha.com.smartanalytics.commonutils;

import android.util.Log;

/**
 * Common class to print logs
 * 
 * @author b0089798
 *
 */
public class LogUtils {

	// local static member which enables/disables logs on ddms
	static boolean sEnableLogs = true;

	/**
	 * prints log.d
	 * 
	 * @param tag
	 * @param msg
	 */
	public static void logD(String tag, String msg) {
		if (sEnableLogs) {
			Log.d(" PROMOTER'S APP**" + tag + " **** ", " *** " + msg
					+ "***** ");
		}
	}

	/**
	 * prints log.d
	 * 
	 * @param tag
	 * @param msg
	 */
	public static void logE(String tag, String msg) {
		if (sEnableLogs) {
			Log.e(" PROMOTER'S APP"+ tag + "*****", " ***** " + msg
					+ "***** ");
		}
	}

}
